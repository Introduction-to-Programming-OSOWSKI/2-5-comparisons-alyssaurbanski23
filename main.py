def greaterThan(x , y):
    if x > y:
        return True
    else:
        return False

print(greaterThan(16, 10))

def lessThan(x , y):
    if x < y:
        return True
    else:
        return False

print(lessThan(6, 7))

def equalTo(x , y):
    if x == y:
        return True
    else:
        return False

print(equalTo(6, 6))

def greaterOrEqual(x , y):
    if x >= y:
        return True
    else:
        return False

print(greaterOrEqual(10, 10))

def lessOrEqual(x , y):
    if x <= y:
        return True
    else:
        return False

print(lessOrEqual(15, 15))